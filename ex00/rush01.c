#include "ft_putchar.h"

void	rush(int x, int y)
{
	char	t;
	int		i[2];
	int		j[2];

	i[0] = 0;
	while (i[0] <= y)
	{
		i[1] = 0;
		while (i[1] <= x)
		{
			t = ' ';
			j[0] = (i[1] == 0 || i[1] == x);
			j[1] = (i[0] == 0 || i[0] == y);
			if (j[0])
				t = '|';
			if (j[1])
				t = '-';
			if (j[0] && j[1])
				t = 'o';
			ft_putchar(t);
			i[1]++;
		}
		ft_putchar('\n');
		i[0]++;
	}
}
