#include "ft_putchar.h"

void	rush(int x, int y)
{
	char	c;
	int		i[2];
	int		edge[2];

	i[0] = 0;
	while (i[0] < y)
	{
		i[1] = 0;
		while (i[1] < x)
		{
			c = ' ';
			edge[0] = (-2 * (i[1] == 0) + (i[1] == x - 1));
			edge[1] = (-2 * (i[0] == 0) + (i[0] == y - 1));
			if (edge[0] || edge[1])
				c = '*';
			if ((edge[0] < 0 && edge[1] < 0) || (edge[0] > 0 && edge[1] > 0))
				c = '/';
			if ((edge[0] > 0 && edge[1] < 0) || (edge[0] < 0 && edge[1] > 0))
				c = '\\';
			ft_putchar(c);
			i[1]++;
		}
		ft_putchar('\n');
		i[0]++;
	}
}
